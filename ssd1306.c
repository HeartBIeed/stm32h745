#include "ssd1306.h"

uint8_t framebuffer[8][128]; // 8 page / 128 column X

void SSD1306_cmd(uint8_t cmd){

    uint8_t arr[2];
    arr[0] = 0x00;
    arr[1] = cmd;
    I2C_writeByte(OLED_ADDR,arr,2);
}

void SSD1306_data(uint8_t data){
    
    uint8_t arr[2];
    arr[0] = 0x40;
    arr[1] = data;
    I2C_writeByte(OLED_ADDR,arr,2);
}

void SSD1306_inverse(uint8_t state){

	if (state) SSD1306_cmd(0xA7);// Включить инверсию
	else 	   SSD1306_cmd(0xA6);// Выключить инверсию
}

void SSD1306_init(){

uint8_t init_commands_list[27] ={
    0xAE, // display off
    0x20, // addressing mode
    0x00, // horizontal mode
    0xB0, // page start
    0xC8, // scan direction
    0x00, // low column
    0x10, // high column
    0x40, // start line
    0x81, // contrast
    0xFF,
    0xA1, // segment remap
    0xA6, // normal display
    0xA8, // multiplex
    0x3F, // 64 lines
    0xD3, // offset
    0x00,
    0xD5, // clock
    0x80,
    0xD9, // precharge
    0xF1,
    0xDA, // com pins
    0x12,
    0xDB, // vcom detect
    0x40,
    0x8D, // charge pump
    0x14,
    0xAF // display on
    };

    for (int i = 0; i < 27; ++i){

    SSD1306_cmd(init_commands_list[i]);
    }
}

void SSD1306_fill(uint8_t pixel){

    SSD1306_cmd(0x21);//set column address
    SSD1306_cmd(0x00);// start 0
    SSD1306_cmd(0x7F);// end dec 127

    SSD1306_cmd(0x22);//set page address
    SSD1306_cmd(0x00); // 0 page
    SSD1306_cmd(0x07); // 7 page

    for (uint16_t i = 0; i < (128*64/8); i++){

    SSD1306_data(pixel);
    }
}

void SSD1306_draw(uint8_t x, uint8_t page, uint8_t width, uint8_t height){

    SSD1306_cmd(0x21);//set column address
    SSD1306_cmd(x);// start 0
    SSD1306_cmd(width);// end dec 127 0x7F

    SSD1306_cmd(0x22);//set page address
    SSD1306_cmd(page); // 0 page
    SSD1306_cmd(height); // 7 page


    for (uint8_t i = page; i < height + 1; i++){

    	for (uint8_t j = x; j < width + 1; j++){

		SSD1306_data(framebuffer[i][j]);

		}
    }
}

void char_to_buffer(char c, uint8_t page, uint8_t x){

	const uint8_t (*font)[5] = ssd1306_font5x7; //указатель на массив

	for (uint8_t i = 0; i < 5; ++i){ //5 column on glyph
	
	framebuffer[page][x+i] = font[c-32][i];// c-32 address symbol / i address column
	}

}

void SSD1306_drawString(const char* str, uint8_t page_start, uint8_t x_start){

	uint8_t max_x = 127-2;
	uint8_t max_page = 7;

	uint8_t x = x_start;
	uint8_t page = page_start;

	while(*str){

		if (x > max_x ) {

		x = 0; // сдвиг каретки
		page = page + 1; // перенос на след страницу
		}

		if (page > max_page) page = 0;

		char_to_buffer(*str, page,x);
		x = x + 6;
		str++; //сдвиг указателя 
	}

	SSD1306_draw(0, page, 127, page);
}


void SSD1306_drawProgress(uint8_t input_dec, uint8_t page){
	
uint8_t fill_col = 0xFF; //  ||||||
uint8_t space_col = 0x81; // ::::::
uint8_t column_line;
uint8_t procent = (input_dec * 127 + 50) / 100;
uint8_t x = 0;

	framebuffer[page][0] = fill_col;

	for (uint8_t i = 1; i < 127; ++i){ 
	
		if (i < procent) column_line = fill_col;
		else column_line = space_col;

	framebuffer[page][x+i] = column_line;
	}

	framebuffer[page][127] = fill_col;
	SSD1306_draw(0, 0,127,7);
}

